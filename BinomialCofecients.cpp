long long po(int base, int e) {
    if (e == 0) {
        return 1;
    }
    
    long long half = po(base, e / 2);
    half = (half * half) % MOD; 

    if (e % 2 != 0) {
        half = (half * base) % MOD; 
    }

    return half;
}
